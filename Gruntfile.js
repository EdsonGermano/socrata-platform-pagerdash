'use strict';

var fs = require('fs');
var LIVERELOAD_PORT = 35729;
var lrSnippet = require('connect-livereload')({port: LIVERELOAD_PORT});
var mountFolder = function (connect, dir) {
    return connect.static(require('path').resolve(dir));
};

var modRewrite = require('connect-modrewrite');
var mrwSnippet = modRewrite([
        '!\\.html|\\.js|\\.css|\\.png|\\.ico|\\.eot|\\.svg|\\.ttf|\\.woff$ /index.html [L]'
    ]);

var connectProxy = require('grunt-connect-proxy/lib/utils');
var proxySnippet = function(req, res, next) {
    if(req && connectProxy.matchContext('/pagerduty', req.url)) {
        if(fs.existsSync('./config.js')) {
            var config = require('./config.js');
            req.headers['Authorization'] = 'Token token='+config.access.authToken;
        }
        connectProxy.proxyRequest(req, res, next);
    } else {
        next()
    }
}

require('grunt-connect-proxy/lib/utils').proxyRequest;

var getProxies = function() {
    var proxies = [];
    if(fs.existsSync('./config.js')) {
        var config = require('./config.js');
        var options = {};
        options = {
            context: '/pagerduty',
            host: config.access.subdomain + '.pagerduty.com',
            https: true,
            port: 443,
            changeOrigin: true,
            rewrite: {
                '^/pagerduty': '/api/v1'
            }
        };
        proxies.push(options);
    }
    return proxies;
};

